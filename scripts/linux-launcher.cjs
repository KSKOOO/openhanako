function sanitizeExecutableName(value) {
  return String(value || "").toLowerCase().replace(/[^a-z0-9+.-]+/g, "-");
}

function getLinuxExecutableName(pkg = {}) {
  return pkg.build?.linux?.executableName
    || pkg.build?.executableName
    || sanitizeExecutableName(pkg.name || pkg.build?.productName || "hanako");
}

function getLinuxBinaryName(executableName) {
  return `${executableName}-bin`;
}

function escapeForDoubleQuotes(value) {
  return String(value || "")
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\$/g, "\\$")
    .replace(/`/g, "\\`");
}

function buildLinuxLauncher({ productName, binaryName, appDir = null, desktopName = null }) {
  const appName = escapeForDoubleQuotes(productName || "Hanako");
  const targetBinary = escapeForDoubleQuotes(binaryName || "hanako-bin");
  const targetDesktopName = escapeForDoubleQuotes(desktopName || `${sanitizeExecutableName(productName || "Hanako")}.desktop`);
  const resolvedAppDir = appDir ? escapeForDoubleQuotes(appDir) : null;

  return [
    "#!/bin/sh",
    "unset ELECTRON_RUN_AS_NODE",
    `APP_NAME="${appName}"`,
    `APP_DESKTOP_NAME="${targetDesktopName}"`,
    'DIR="$(CDPATH= cd -- "$(dirname "$0")" && pwd)"',
    resolvedAppDir ? `APP_DIR="${resolvedAppDir}"` : 'APP_DIR="$DIR"',
    'SANDBOX="$APP_DIR/chrome-sandbox"',
    `BIN="$APP_DIR/${targetBinary}"`,
    "",
    "can_write_dir() {",
    '  dir="$1"',
    '  [ -n "$dir" ] || return 1',
    '  [ -d "$dir" ] || return 1',
    '  [ -w "$dir" ] || return 1',
    '  probe="$dir/.hanako-write-test-$$"',
    '  (umask 077 && : > "$probe") 2>/dev/null || return 1',
    '  rm -f "$probe" 2>/dev/null || true',
    "  return 0",
    "}",
    "",
    "cache_root() {",
    '  if [ -n "${HANA_HOME:-}" ]; then',
    '    printf "%s\\n" "$HANA_HOME"',
    "  else",
    '    printf "%s\\n" "${XDG_CACHE_HOME:-$HOME/.cache}/hanako"',
    "  fi",
    "}",
    "",
    "ensure_runtime_dir() {",
    '  if can_write_dir "${XDG_RUNTIME_DIR:-}"; then',
    "    return 0",
    "  fi",
    '  fallback="$(cache_root)/xdg-runtime"',
    '  mkdir -p "$fallback" 2>/dev/null || return 0',
    '  chmod 700 "$fallback" 2>/dev/null || true',
    '  if can_write_dir "$fallback"; then',
    '    export XDG_RUNTIME_DIR="$fallback"',
    "  fi",
    "}",
    "",
    "ensure_tmp_dir() {",
    '  if can_write_dir "${TMPDIR:-}"; then',
    "    return 0",
    "  fi",
    "  if can_write_dir /tmp; then",
    "    return 0",
    "  fi",
    '  fallback="$(cache_root)/tmp"',
    '  mkdir -p "$fallback" 2>/dev/null || return 0',
    '  chmod 700 "$fallback" 2>/dev/null || true',
    '  if can_write_dir "$fallback"; then',
    '    export TMPDIR="$fallback"',
    '    export TEMP="$fallback"',
    '    export TMP="$fallback"',
    "  fi",
    "}",
    "",
    "userns_enabled() {",
    '  case "${HANAKO_FORCE_USERNS:-}" in',
    '    1|true|TRUE|yes|YES) return 0 ;;',
    '    0|false|FALSE|no|NO) return 1 ;;',
    "  esac",
    "  if command -v unshare >/dev/null 2>&1; then",
    "    unshare -Ur true >/dev/null 2>&1 && return 0",
    "  fi",
    "  if [ -r /proc/sys/kernel/unprivileged_userns_clone ]; then",
    '    [ "$(cat /proc/sys/kernel/unprivileged_userns_clone 2>/dev/null)" = "1" ] && return 0',
    "  fi",
    "  if [ -r /proc/sys/user/max_user_namespaces ]; then",
    '    max_userns="$(cat /proc/sys/user/max_user_namespaces 2>/dev/null)"',
    '    [ -n "$max_userns" ] && [ "$max_userns" != "0" ] && return 0',
    "  fi",
    "  return 1",
    "}",
    "",
    "sandbox_helper_status() {",
    '  if [ ! -e "$SANDBOX" ]; then',
    '    printf "%s\\n" "missing"',
    "    return 1",
    "  fi",
    '  if [ ! -x "$SANDBOX" ]; then',
    '    printf "%s\\n" "not-executable"',
    "    return 1",
    "  fi",
    "  if ! command -v stat >/dev/null 2>&1; then",
    '    printf "%s\\n" "stat-unavailable"',
    "    return 1",
    "  fi",
    '  sandbox_uid="$(stat -c "%u" "$SANDBOX" 2>/dev/null || true)"',
    '  sandbox_mode="$(stat -c "%a" "$SANDBOX" 2>/dev/null || true)"',
    '  if [ "$sandbox_uid" != "0" ]; then',
    '    printf "%s\\n" "owner:${sandbox_uid:-unknown}"',
    "    return 1",
    "  fi",
    '  case "$sandbox_mode" in',
    '    4???|5???|6???|7???) printf "%s\\n" "ok"; return 0 ;;',
    "  esac",
    '  printf "%s\\n" "mode:${sandbox_mode:-unknown}"',
    "  return 1",
    "}",
    "",
    "make_temp_dir() {",
    '  base="$(cache_root)/launcher"',
    '  mkdir -p "$base" 2>/dev/null || return 1',
    "  if command -v mktemp >/dev/null 2>&1; then",
    '    mktemp -d "$base/run.XXXXXX" 2>/dev/null && return 0',
    "  fi",
    '  ts="$(date +%s 2>/dev/null || printf "%s\\n" 0)"',
    '  fallback="$base/run-$$-$ts"',
    '  (umask 077 && mkdir "$fallback") 2>/dev/null || return 1',
    '  printf "%s\\n" "$fallback"',
    "}",
    "",
    "sandbox_failure_matches() {",
    '  log_file="$1"',
    '  [ -f "$log_file" ] || return 1',
    '  grep -E "No usable sandbox!|zygote_host_impl_linux\\.cc:132|setuid_sandbox_host\\.cc:[0-9]+|SUID sandbox helper binary was found, but is not configured correctly" "$log_file" >/dev/null 2>&1',
    "}",
    "",
    "run_with_sandbox_recovery() {",
    "  RUN_WITH_RECOVERY_STATUS=0",
    '  if [ "${HANAKO_DISABLE_SANDBOX_RETRY:-0}" = "1" ]; then',
    '    "$BIN" "$@"',
    "    RUN_WITH_RECOVERY_STATUS=$?",
    '    return "$RUN_WITH_RECOVERY_STATUS"',
    "  fi",
    '  launch_tmp="$(make_temp_dir)" || {',
    '    "$BIN" "$@"',
    "    RUN_WITH_RECOVERY_STATUS=$?",
    '    return "$RUN_WITH_RECOVERY_STATUS"',
    "  }",
    '  stderr_pipe="$launch_tmp/stderr.pipe"',
    '  stderr_log="$launch_tmp/stderr.log"',
    '  if ! mkfifo "$stderr_pipe" 2>/dev/null; then',
    '    rm -rf "$launch_tmp" 2>/dev/null || true',
    '    "$BIN" "$@"',
    "    RUN_WITH_RECOVERY_STATUS=$?",
    '    return "$RUN_WITH_RECOVERY_STATUS"',
    "  fi",
    '  tee "$stderr_log" < "$stderr_pipe" >&2 &',
    "  tee_pid=$!",
    '  started_at="$(date +%s 2>/dev/null || printf "%s\\n" 0)"',
    '  "$BIN" "$@" 2>"$stderr_pipe"',
    "  status=$?",
    '  ended_at="$(date +%s 2>/dev/null || printf "%s\\n" 0)"',
    '  wait "$tee_pid" >/dev/null 2>&1 || true',
    '  rm -f "$stderr_pipe" 2>/dev/null || true',
    "  duration=0",
    '  case "$started_at" in',
    '    ""|*[!0-9]*) started_at=0 ;;',
    "  esac",
    '  case "$ended_at" in',
    '    ""|*[!0-9]*) ended_at=0 ;;',
    "  esac",
    '  if [ "$ended_at" -ge "$started_at" ] 2>/dev/null; then',
    '    duration=$((ended_at - started_at))',
    "  fi",
    "  RUN_WITH_RECOVERY_STATUS=$status",
    '  if [ "$status" -ne 0 ] && [ "$duration" -le "${HANAKO_SANDBOX_RETRY_WINDOW:-15}" ] && sandbox_failure_matches "$stderr_log"; then',
    '    rm -rf "$launch_tmp" 2>/dev/null || true',
    "    return 125",
    "  fi",
    '  rm -rf "$launch_tmp" 2>/dev/null || true',
    '  return "$RUN_WITH_RECOVERY_STATUS"',
    "}",
    "",
    "run_standard_launch() {",
    '  run_with_sandbox_recovery "$@"',
    "  launch_status=$?",
    '  if [ "$launch_status" -eq 125 ]; then',
    '    if [ "${HANAKO_DISABLE_NO_SANDBOX_FALLBACK:-0}" != "1" ]; then',
    '      echo "[$APP_NAME] Chromium sandbox initialization failed during startup; retrying with --no-sandbox for compatibility." >&2',
    '      export HANAKO_ELECTRON_SANDBOX_FALLBACK=no-sandbox',
    '      exec "$BIN" --no-sandbox "$@"',
    "    fi",
    '    exit "${RUN_WITH_RECOVERY_STATUS:-1}"',
    "  fi",
    '  exit "$launch_status"',
    "}",
    "",
    "run_disable_setuid_launch() {",
    '  run_with_sandbox_recovery --disable-setuid-sandbox "$@"',
    "  launch_status=$?",
    '  if [ "$launch_status" -eq 125 ]; then',
    '    if [ "${HANAKO_DISABLE_NO_SANDBOX_FALLBACK:-0}" != "1" ]; then',
    '      echo "[$APP_NAME] Chromium sandbox initialization still failed with --disable-setuid-sandbox; retrying with --no-sandbox for compatibility." >&2',
    '      export HANAKO_ELECTRON_SANDBOX_FALLBACK=no-sandbox',
    '      exec "$BIN" --no-sandbox "$@"',
    "    fi",
    '    exit "${RUN_WITH_RECOVERY_STATUS:-1}"',
    "  fi",
    '  exit "$launch_status"',
    "}",
    "",
    "ensure_runtime_dir",
    "ensure_tmp_dir",
    "",
    'if [ -n "${WAYLAND_DISPLAY:-}" ] && [ -z "${ELECTRON_OZONE_PLATFORM_HINT:-}" ] && [ "${HANAKO_DISABLE_OZONE_HINT:-0}" != "1" ]; then',
    '  export ELECTRON_OZONE_PLATFORM_HINT=auto',
    "fi",
    'if [ -z "${CHROME_DESKTOP:-}" ]; then',
    '  export CHROME_DESKTOP="$APP_DESKTOP_NAME"',
    "fi",
    "",
    'if [ ! -x "$BIN" ]; then',
    '  echo "$APP_NAME is not installed correctly: $BIN is missing or not executable." >&2',
    "  exit 1",
    "fi",
    "",
    'if [ "${HANAKO_FORCE_NO_SANDBOX:-0}" = "1" ]; then',
    '  export HANAKO_ELECTRON_SANDBOX_FALLBACK=no-sandbox',
    '  exec "$BIN" --no-sandbox "$@"',
    "fi",
    "",
    'sandbox_status="$(sandbox_helper_status)"',
    'if [ "$sandbox_status" = "ok" ]; then',
    '  run_standard_launch "$@"',
    "fi",
    "",
    "if userns_enabled; then",
    '  echo "[$APP_NAME] chrome-sandbox is unusable ($sandbox_status); falling back to --disable-setuid-sandbox for compatibility." >&2',
    '  export HANAKO_ELECTRON_SANDBOX_FALLBACK=disable-setuid-sandbox',
    '  run_disable_setuid_launch "$@"',
    "fi",
    "",
    'if [ "${HANAKO_DISABLE_NO_SANDBOX_FALLBACK:-0}" != "1" ]; then',
    '  echo "[$APP_NAME] chrome-sandbox is unusable ($sandbox_status) and user namespaces are unavailable; falling back to --no-sandbox for compatibility." >&2',
    '  export HANAKO_ELECTRON_SANDBOX_FALLBACK=no-sandbox',
    '  exec "$BIN" --no-sandbox "$@"',
    "fi",
    "",
    'exec "$BIN" "$@"',
    "",
  ].join("\n");
}

module.exports = {
  sanitizeExecutableName,
  getLinuxExecutableName,
  getLinuxBinaryName,
  buildLinuxLauncher,
};
